export const getScenarioById = (id: string): ScenarioTemplate =>
  scenarioTemplates.find((scenarioItem) => scenarioItem.id === id) ?? scenarioTemplates[0]

export const cloneScenario = (scenarioItem: ScenarioTemplate): ScenarioTemplate =>
  JSON.parse(JSON.stringify(scenarioItem)) as ScenarioTemplate
