import type { EdgeType, NodeType, ScenarioTemplate, SimulationEdge, SimulationNode, SystemType } from '../types/simulation'

const node = (
  id: string,
  label: string,
  type: NodeType,
  x: number,
  y: number,
  overrides: Partial<SimulationNode['properties']> = {},
): SimulationNode => ({
  id,
  label,
  type,
  x,
  y,
  properties: {
    capacity: 100,
    demand: 55,
    throughput: 70,
    failureProbability: 0.04,
    recoveryTime: 3,
    cost: 30,
    priority: 5,
    resilience: 65,
    stock: 50,
    ...overrides,
  },
})

const edge = (
  id: string,
  label: string,
  source: string,
  target: string,
  type: EdgeType = 'flow',
  overrides: Partial<SimulationEdge['properties']> = {},
): SimulationEdge => ({
  id,
  label,
  source,
  target,
  type,
  properties: {
    flowCapacity: 80,
    travelTime: 1,
    cost: 10,
    reliability: 0.92,
    direction: 'directed',
    congestionSensitivity: 0.45,
    transferEfficiency: 0.88,
    dependencyStrength: 0.65,
    ...overrides,
  },
})

